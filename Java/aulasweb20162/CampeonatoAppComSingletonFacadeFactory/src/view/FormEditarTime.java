    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      try{
        int codigo = Integer.valueOf(jTextField1.getText());
        TimeBS timeBS = new TimeBS();
        time = timeBS.consultarTime(codigo);
        jTextField2.setText(time.getNome());
        jComboBox1.setSelectedItem(time.getEstado());
        
        }
      catch (Exception e){
           JOptionPane.showMessageDialog(null, e.getMessage());
      }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
       try{
            int codigo = Integer.valueOf(jTextField1.getText());
            String nome = jTextField2.getText();
            String estado = jComboBox1.getSelectedItem().toString();

            Time time = new Time(codigo,nome,estado);
            TimeBS timeBS = new TimeBS();
            timeBS.editarTime(time);
            jButtonEditar.setEnabled(true);
            jButtonSalvar.setEnabled(false);


        }
       catch (Exception e){
             JOptionPane.showMessageDialog(null, e.getMessage());
       }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

